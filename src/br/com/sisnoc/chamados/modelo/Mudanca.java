package br.com.sisnoc.chamados.modelo;

public class Mudanca {
	
	private String id;
	private String mudanca;
	private String resumo;
	private String agendamento;
	private String statusDescricao;
	private String responsavel;
	private String loginResponsavel;
	private String equipe;
	private String ccm;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMudanca() {
		return mudanca;
	}
	public void setMudanca(String mudanca) {
		this.mudanca = mudanca;
	}
	public String getResumo() {
		return resumo;
	}
	public void setResumo(String resumo) {
		this.resumo = resumo;
	}
	public String getAgendamento() {
		return agendamento;
	}
	public void setAgendamento(String agendamento) {
		this.agendamento = agendamento;
	}
	public String getStatusDescricao() {
		return statusDescricao;
	}
	public void setStatusDescricao(String statusDescricao) {
		this.statusDescricao = statusDescricao;
	}
	public String getResponsavel() {
		return responsavel;
	}
	public void setResponsavel(String responsavel) {
		this.responsavel = responsavel;
	}
	public String getLoginResponsavel() {
		return loginResponsavel;
	}
	public void setLoginResponsavel(String loginResponsavel) {
		this.loginResponsavel = loginResponsavel;
	}
	public String getEquipe() {
		return equipe;
	}
	public void setEquipe(String equipe) {
		this.equipe = equipe;
	}
	public String getCcm() {
		return ccm;
	}
	public void setCcm(String ccm) {
		this.ccm = ccm;
	}

	

}